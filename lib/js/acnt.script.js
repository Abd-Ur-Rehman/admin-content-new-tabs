//********************************************************************************************************************************
// now start the engine
//********************************************************************************************************************************
jQuery(document).ready( function($) {

//********************************************************************************************************************************
// quick helper to check for an existance of an element
//********************************************************************************************************************************
	$.fn.divExists = function(callback) {
		// slice some args
		var args = [].slice.call( arguments, 1 );
		// check for length
		if ( this.length ) {
			callback.call( this, args );
		}
		// return it
		return this;
	};

//********************************************************************************************************************************
// add a target blank on view page buttons
//********************************************************************************************************************************
	$( 'div#edit-slug-box span#view-post-btn' ).divExists( function() {
		$( this ).find( 'a.button' ).attr( 'target', '_blank' );
	});

	$( 'li#wp-admin-bar-view' ).divExists( function() {
		$( this ).find( 'a.ab-item' ).attr( 'target', '_blank' );
	});

//********************************************************************************************************************************
//  add target blank to view links on post table
//********************************************************************************************************************************
	$( 'table.wp-list-table' ).divExists( function() {
		$( this ).find( 'div.row-actions' ).each(function() {
			$( this ).find( 'span.view a' ).attr( 'target', '_blank' );
		});
	});

//********************************************************************************************************************************
//  add target blank to edit post
//********************************************************************************************************************************
	$( 'li#wp-admin-bar-edit' ).divExists( function() {
		$( this ).find( 'a.ab-item' ).attr( 'target', '_blank' );
	});

//********************************************************************************************************************************
//  add target blank to sample permalink
//********************************************************************************************************************************
	$( 'span#sample-permalink' ).divExists( function() {
		$( this ).find( 'a' ).attr( 'target', '_blank' );
	});

//********************************************************************************************************************************
//  add target blank to view site
//********************************************************************************************************************************
	$( 'li#wp-admin-bar-site-name' ).divExists( function() {
		$( this ).find( 'a.ab-item' ).attr( 'target', '_blank' );
	});

//********************************************************************************************************************************
// that's all folks. we're done here
//********************************************************************************************************************************
});